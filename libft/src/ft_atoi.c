#include "libft.h"

int	ft_atoi(const char *str)
{
	int sign;
	int tot;

	sign = 1;
	tot = 0;
	if (!str)
		return (0);
	while (ft_isspace(*str))
		str++;
	if (*str == '-' || *str == '+')
	{
		if (*str == '-')
			sign = -1;
		str++;
	}
	while (ft_isdigit(*str))
	{
		tot = tot * 10 + (*str - '0');
		str++;
	}
	return (tot * sign);
}
